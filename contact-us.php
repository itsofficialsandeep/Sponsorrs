		function sendMessage() {

			const message = $('#messageBox').val();
			const actionToken = $("#messageBox").attr('action-token');

			if (!message) {
				// show dialog box when user is not loginned
				Swal.fire({
					showConfirmButton: false,
					title: '<h5 class="fs-5 text-primary">Alert!</h5>',
					html:
						'<p class="fs-5">Please type the message</p>',
				});
			} else {
				var formData = {
					message: message,
					actionToken: 'actionToken',	//abp
					action: 1
				};

				// call the server
				$.ajax({
					type: "POST",
					url: "/profile-functions/for-about-and-contact.php",
					data: formData,
					success: function (response) {
						console.log(response);
						if (response.code == 200) {
							$('#messageBox').val(" ");
							$("#sendButton").text("Feedback sent..").removeClass("btn-primary").addClass("btn-success");
							console.log(response);
						} else {
							$("#sendButton").text("Failed to provide feedback!").removeClass("btn-primary").addClass("btn-warning");
						}
					},
					error: function (error) {
						console.error(error);
					},
				});
			}

			$("#sendButton").mouseleave(function () {
				$("#sendButton").text("Send Message").removeClass("btn-warning").removeClass("btn-success").addClass("btn-primary");
			});
		}


	</script>
